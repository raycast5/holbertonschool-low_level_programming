#include <stdio.h>
#include "3-calc.h"

/**
 * op_add - adds @a and @b
 *
 * @a: an int
 * @b: an int
 * Return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - substracts @a and @b
 *
 * @a: an int
 * @b: an int
 * Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies @a and @b
 *
 * @a: an int
 * @b: an int
 * Return: int
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides @a and @b
 *
 * @a: an int
 * @b: an int
 * Return: int
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - returns remainder of @a and @b
 *
 * @a: an int
 * @b: an int
 * Return: int
 */
int op_mod(int a, int b)
{
	return (a % b);
}



